    function GetRTime(obj, endFunc) {
        var NowTime = new Date(obj.attr('data-time'));
        var nMS = NowTime.getTime() - new Date().getTime();
        var nD = Math.floor(nMS / (1000 * 60 * 60 * 24));
        var nH = Math.floor(nMS / (1000 * 60 * 60)) % 24;
        var nM = Math.floor(nMS / (1000 * 60)) % 60;
        var nS = Math.floor(nMS / 1000) % 60;
        // var nMS = Math.floor(nMS / 10) % 100;
        if(nMS>=0){
            if(nH < 10) {
                nH = '0' + nH;
            }
            if(nM < 10) {
                nM = '0' + nM;
            }
            if(nS<10){
                nS = '0'+nS;
